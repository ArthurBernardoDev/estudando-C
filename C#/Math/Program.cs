using System;

namespace Math
{
    class Program
    {
        static void Main(string[] args)
        {
         // Math.Max(x,y) usado apra encontrar o maior valor de x e your
         Console.WriteLine(Math.Max(5, 10);

         // Math.Min(x,y) usado para encontrar o menor valor de x e y
        Console.WriteLine(Math.Min(5,10));
        
        // Math.Sqrt(x) Raiz quadrada de x
        Math.Sqrt(64);

        // Math.Round() arrenda um número para o Número inteiro mais próximo
        Math.Round(9.99);
        }
    }
}
